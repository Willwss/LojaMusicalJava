    private void preencherTabelaCorda(){
        try {

            InstrumentoCordaServicos cs = ServicosFactory.getInstrumentoCordasServicos();

            ArrayList<InstrumentoCordasVO> cordas = new ArrayList<>();

            cordas = cs.buscarInstrumentoCordas();

            for (int i = 0; i < cordas.size(); i++) {
                dtm.addRow(new String[]{
                    String.valueOf(cordas.get(i).getIdInstrumentoCorda()),
                    String.valueOf(cordas.get(i).getMarca()),
                    String.valueOf(cordas.get(i).getModelo()),
                    String.valueOf(cordas.get(i).getAno()),
                    String.valueOf(cordas.get(i).getValorCustoCordas()),
                    String.valueOf(cordas.get(i).getQuantidade())
                });
            }//fecha for

            jtableInstrumentoCorda.setModel(dtm);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(
                    this,
                    "Erro! " + e.getMessage());
        }//fecha catch
    }//Fecha preencher tabela corda
    
    private void limparTabelaCorda() {      
        dtm.setNumRows(0);
        
    }//fecha limparTabelaCorda
    
    private void deletarCorda(){
        try {
            int linha = jtableInstrumentoCorda.getSelectedRow();
            
            if(linha == -1){
                JOptionPane.showMessageDialog(
                        rootPane, 
                        "Selecione uma linha!");
            }else{
                String id = jtableInstrumentoCorda.getValueAt(linha, 0).toString();
                InstrumentoCordaServicos CS = ServicosFactory.getInstrumentoCordasServicos();
                CS.deletarInstrumentoCorda(Long.parseLong(id));
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(
                    rootPane,
                    "Erro! "+e.getMessage());
        }
    }//fecha deletarCorda
    
    private void filtrarCorda(){
        try {
            
            if(jtPesq.getText() .isEmpty()){
                limparTabelaCorda();
                preencherTabelaCorda();
            }else{
                
                String query = "";
                String filtro = jcomboFiltroCorda.getSelectedItem().toString();
                
                if(filtro.equalsIgnoreCase("Marca")){
                    query = "where marca like '%"+jtPesq.getText()+"%'";
                }else if (filtro.equalsIgnoreCase("Modelo")){
                    query = "where modelo like '%"+jtPesq.getText()+"%'";
                }else if (filtro.equalsIgnoreCase("Codigo")){
                    query = "where idinstrumentocorda = "+jtPesq.getText();
                }else if(filtro.equalsIgnoreCase("Valor")){
                    query = "where valorcusto like '%"+jtPesq.getText()+"%'";
                }else if(filtro.equalsIgnoreCase("Quantidade")){
                    query = "where quantidade like '%"+jtPesq.getText()+"%'";
                }else if(filtro.equalsIgnoreCase("Ano")){
                    query = "where ano like '%"+jtPesq.getText()+"%'";
                }//fecha else if
                
                InstrumentoCordaServicos CS = ServicosFactory.getInstrumentoCordasServicos();
                ArrayList<InstrumentoCordasVO> cordas = CS.filtrar(query);
                
                for(int i=0 ; i<cordas.size() ; i++){
                    dtm.addRow(new String[]{
                        String.valueOf(cordas.get(i).getIdInstrumentoCorda()),
                        cordas.get(i).getMarca(),
                        cordas.get(i).getModelo(),
                        String.valueOf(cordas.get(i).getAno()),
                        String.valueOf(cordas.get(i).getValorCustoCordas()),
                        String.valueOf(cordas.get(i).getQuantidade()),
                    });
                }
                
                jtableInstrumentoCorda.setModel(dtm);
                
            }//fecha else
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(
                    rootPane,
                    "Erro "+e.getMessage());
        }
    }//fecha filtrarCorda
    
    private void alterarInstrumentoCorda(){
       int linha = jtableInstrumentoCorda.getSelectedRow();
        String codigo = jtableInstrumentoCorda.getValueAt(linha, 0).toString();
        String marca = jtableInstrumentoCorda.getValueAt(linha, 1).toString();
        String modelo = jtableInstrumentoCorda.getValueAt(linha, 2).toString();
        String ano = jtableInstrumentoCorda.getValueAt(linha, 3).toString();
        String valorCusto = jtableInstrumentoCorda.getValueAt(linha, 4).toString();
        String quantidade = jtableInstrumentoCorda.getValueAt(linha, 5).toString();

        jtCodigoCorda.setText(codigo);
        jtMarcaCorda.setText(marca);
        jtModeloCorda.setText(modelo);
        jtAnoCorda.setText(ano);
        jtValorCustoCorda.setText(valorCusto);
        jtQuantidadeCorda.setText(quantidade);
        
    }//Fecha alterarInstrumentoCorda
    
    private void confirmarAlteracaoCorda() {
        try {
            InstrumentoCordasVO cVO = new InstrumentoCordasVO();
            cVO.setIdInstrumentoCorda(Long.parseLong(jtCodigoCorda.getText()));
            cVO.setMarca(jtMarcaCorda.getText());
            cVO.setModelo(jtModeloCorda.getText());
            cVO.setAno(Integer.parseInt(jtAnoCorda.getText()));
            cVO.setValorCustoCordas(Double.parseDouble(jtValorCustoCorda.getText()));
            cVO.setQuantidade(Integer.parseInt(jtQuantidadeCorda.getText()));

            InstrumentoCordaServicos cs = servicos.ServicosFactory.getInstrumentoCordasServicos();
            cs.alterarInstrumentoCorda(cVO);

            JOptionPane.showMessageDialog(
                    rootPane,
                    "Instrumento alterado com sucesso!");

        } catch (Exception e) {
            JOptionPane.showMessageDialog(
                    rootPane,
                    "Erro! " + e.getMessage(),
                    "erro",
                    JOptionPane.ERROR_MESSAGE);
        }//fecha catch
    }//Fecha confirmarAlteracaoCorda
    
