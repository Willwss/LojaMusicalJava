    private void preencherTabelaPerc(){
        try {

            InstrumentoPercussaoServicos ps = ServicosFactory.getInstrumentoPercussaoServicos();

            ArrayList<InstrumentoPercussaoVO> perc = new ArrayList<>();

            perc = ps.buscarInstrumentoPercussao();

            for (int i = 0; i < perc.size(); i++) {
                dtm.addRow(new String[]{
                    String.valueOf(perc.get(i).getIdInstrumentoPercussao()),
                    String.valueOf(perc.get(i).getMarca()),
                    String.valueOf(perc.get(i).getModelo()),
                    String.valueOf(perc.get(i).getAno()),
                    String.valueOf(perc.get(i).getValorcusto()),
                    String.valueOf(perc.get(i).getQuantidade())
                });
            }//fecha for

            jtableInstumentoPerc.setModel(dtm);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(
                    this,
                    "Erro! " + e.getMessage());
        }//fecha catch
    }//Fecha preencher tabela corda
    
    private void limparTabelaPercussao() {      
        dtm.setNumRows(0);
        
    }//fecha limparTabelaCorda
    
    private void deletarPercussao(){
        try {
            int linha = jtableInstumentoPerc.getSelectedRow();
            
            if(linha == -1){
                JOptionPane.showMessageDialog(
                        rootPane, 
                        "Selecione uma linha!");
            }else{
                String id = jtableInstumentoPerc.getValueAt(linha, 0).toString();
                InstrumentoPercussaoServicos PS = ServicosFactory.getInstrumentoPercussaoServicos();
                PS.deletarInstrumentoPercussao(Long.parseLong(id));
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(
                    rootPane,
                    "Erro! "+e.getMessage());
        }
    }//fecha deletarCorda
    
    private void filtrarPercussao(){
        try {
            
            if(jtPesq.getText() .isEmpty()){
                limparTabelaPercussao();
                preencherTabelaPerc();
            }else{
                
                String query = "";
                String filtro = jcomboFiltroPerc.getSelectedItem().toString();
                
                if(filtro.equalsIgnoreCase("Marca")){
                    query = "where marca like '%"+jtPesq.getText()+"%'";
                }else if (filtro.equalsIgnoreCase("Modelo")){
                    query = "where modelo like '%"+jtPesq.getText()+"%'";
                }else if (filtro.equalsIgnoreCase("Codigo")){
                    query = "where idinstrumentopercussao = "+jtPesq.getText();
                }else if(filtro.equalsIgnoreCase("Valor")){
                    query = "where valorcusto like '%"+jtPesq.getText()+"%'";
                }else if(filtro.equalsIgnoreCase("Quantidade")){
                    query = "where quantidade like '%"+jtPesq.getText()+"%'";
                }else if(filtro.equalsIgnoreCase("Ano")){
                    query = "where ano like '%"+jtPesq.getText()+"%'";
                }//fecha else if
                
                InstrumentoPercussaoServicos PS = ServicosFactory.getInstrumentoPercussaoServicos();
                ArrayList<InstrumentoPercussaoVO> perc = PS.filtrar(query);
                
                for(int i=0 ; i<perc.size() ; i++){
                    dtm.addRow(new String[]{
                        String.valueOf(perc.get(i).getIdInstrumentoPercussao()),
                        perc.get(i).getMarca(),
                        perc.get(i).getModelo(),
                        String.valueOf(perc.get(i).getAno()),
                        String.valueOf(perc.get(i).getValorcusto()),
                        String.valueOf(perc.get(i).getQuantidade()),
                    });
                }
                
                jtableInstumentoPerc.setModel(dtm);
                
            }//fecha else
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(
                    rootPane,
                    "Erro "+e.getMessage());
        }
    }//fecha filtrarCorda
    
    private void alterarInstrumentoPercussao(){
       int linha = jtableInstumentoPerc.getSelectedRow();
        String codigo = jtableInstumentoPerc.getValueAt(linha, 0).toString();
        String marca = jtableInstumentoPerc.getValueAt(linha, 1).toString();
        String modelo = jtableInstumentoPerc.getValueAt(linha, 2).toString();
        String ano = jtableInstumentoPerc.getValueAt(linha, 3).toString();
        String valorCusto = jtableInstumentoPerc.getValueAt(linha, 4).toString();
        String quantidade = jtableInstumentoPerc.getValueAt(linha, 5).toString();

        jtCodigoPerc.setText(codigo);
        jtMarcaPerc.setText(marca);
        jtModeloPerc.setText(modelo);
        jtAnoPerc.setText(ano);
        jtValorCustoPerc.setText(valorCusto);
        jtQuantidadePerc.setText(quantidade);
        
    }//Fecha alterarInstrumentoCorda
    
    private void confirmarAlteracaoPercussao() {
        try {
            InstrumentoPercussaoVO pVO = new InstrumentoPercussaoVO();
            pVO.setIdInstrumentoPercussao(Long.parseLong(jtCodigoPerc.getText()));
            pVO.setMarca(jtMarcaPerc.getText());
            pVO.setModelo(jtModeloPerc.getText());
            pVO.setAno(Integer.parseInt(jtAnoPerc.getText()));
            pVO.setValorcusto(Double.parseDouble(jtValorCustoPerc.getText()));
            pVO.setQuantidade(Integer.parseInt(jtQuantidadePerc.getText()));

            InstrumentoPercussaoServicos ps = servicos.ServicosFactory.getInstrumentoPercussaoServicos();
            ps.alterarInstrumentoPercussao(pVO);

            JOptionPane.showMessageDialog(
                    rootPane,
                    "Instrumento alterado com sucesso!");

        } catch (Exception e) {
            JOptionPane.showMessageDialog(
                    rootPane,
                    "Erro! " + e.getMessage(),
                    "erro",
                    JOptionPane.ERROR_MESSAGE);
        }//fecha catch
    }//Fecha confirmarAlteracaoCorda
    
